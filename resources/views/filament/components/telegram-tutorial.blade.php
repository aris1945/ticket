    <h3>Cara Mendapatkan Telegram Chat ID</h3>
    <p>Ikuti langkah-langkah berikut untuk mendapatkan Telegram Chat ID Anda:</p>
    
    <ul>
        <li>
            <p><strong>Langkah 1:</strong> Buka aplikasi Telegram Anda</p>
        </li>
        <li>
            <p><strong>Langkah 2:</strong> Cari bot <code>@systemticketing_bot</code> atau klik link ini: <a href="https://t.me/systemticketing_bot" target="_blank">https://t.me/systemticketing_bot</a></p>
        </li>
        <li>
            <p><strong>Langkah 3:</strong> Mulai percakapan dengan bot dengan mengirim pesan apapun atau klik tombol "Start"</p>
        </li>
        <li>
            <p><strong>Langkah 4:</strong> Bot akan membalas dengan informasi Anda, termasuk ID numerik Anda. Contoh:</p>
            <pre>Hai, berikut data anda :
ID Telegram : 123456789
Nama : Louise 
Username : @louisevandersaar
</pre>
        </li>
        <li>
            <p><strong>Langkah 5:</strong> Salin angka yang tertera pada baris "ID Telegram:" (dalam contoh ini: 123456789) dan paste pada kolom Telegram Chat ID di bawah</p>
        </li>
    </ul>
